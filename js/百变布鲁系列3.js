<!-- 按键和车 -->
//<script type = "text/javascript" >
$('.text-text-text').find('.img123').css('display', "none")
$('.text-text-text').find('.img123').eq(0).css('display', "block")

// $('.yuan').eq(0).css('bockground','#ffe400')
$('.yuan223').click(function() {
	$('.text-text-text').find('.img123').css('display', "none")

	$('.text-text-text').find('.img123').eq($(this).index()).css('display', "block")
	$('.yuan223').css('background', 'white')
	$(this).css("background", "#ffe400")
	$('.yuan223').css({
		transform: 'scale(1)'
	})
	$(this).css({
		transform: 'scale(1.2)',
		border: "1px solid white"
	})
})
//</script>
<!-- 上边和车 -->
// <script type="text/javascript">
$('.baitiao23').hide()
$('.baitiao23').eq(0).show()
$('.text-text23').hide()
$('.text-text23').eq(0).show()
$('.top123').css({'color':'white',"opacity":"0.5"})
$('.top123').eq(0).css({'color':'white',"opacity":"0.5"})
$('.top123').click(function() {
	$('.text-text23').hide()
	$('.text-text23').eq($(this).index()).show()
	$('.baitiao23').hide()
	$('.baitiao23').eq($(this).index()).show()
	$('.top123').css({'color':'white',"opacity":"0.5"})
	$(this).css({'color':'white',"opacity":"1"})
})
// </script>
